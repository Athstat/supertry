// Helper function to calculate fantasy points based on stats
export const calculateFantasyPoints = (stats: RugbyPlayer["stats"]): number => {
  return (
    stats.tries * 5 +
    stats.assists * 2 +
    stats.tackles * 1 +
    stats.missedTackles * -2 +
    stats.lineBreaks * 3 +
    (stats.goalKickingAccuracy > 0 ? 2 : 0)
  );
};

